    private void populateRegSiteOrgTable() {

        DefaultTableModel dtm = (DefaultTableModel) regSiteOrgJTable.getModel();
        dtm.setRowCount(0);

        for (Organization objOrganization : objRegisteredSiteEnterprise.getObjOrganizationDirectory().getOrganizationList()) {

            Object row[] = new Object[2];
            row[0] = objOrganization.getOrganizationID();
            row[1] = objOrganization;

            dtm.addRow(row);
        }
    }

    private void populateregSiteOrgComboBox() {

        regSiteOrgJComboBox.removeAllItems();

        for (Organization.OrganizationType orgType : Organization.OrganizationType.values()) {

            if (orgType.getValue().equals(Organization.OrganizationType.RegisteredSiteAgent.getValue())) {

                regSiteOrgJComboBox.addItem(orgType);
            }
        }
    }

