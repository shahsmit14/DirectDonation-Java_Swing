    private void addOrgJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrgJButtonActionPerformed

        boolean isOrgPresent = false;

        if (regSiteOrgJComboBox.getItemCount() != 0) {

            Organization.OrganizationType orgTypeSelected = (Organization.OrganizationType) regSiteOrgJComboBox.getSelectedItem();
            System.out.println("Org selected ->" + orgTypeSelected);

            for (Organization objOrgPresent : objRegisteredSiteEnterprise.getObjOrganizationDirectory().getOrganizationList()) {

                System.out.println("Org present ->" + objOrgPresent);
                if (objOrgPresent.getClass().getSimpleName().equals(orgTypeSelected.getValue())) {

                    JOptionPane.showMessageDialog(null, "Organization already exists");
                    isOrgPresent = true;
                    return;
                }
            }

            if (!isOrgPresent) {

                objRegisteredSiteEnterprise.getObjOrganizationDirectory().addOrganization(orgTypeSelected);
                JOptionPane.showMessageDialog(null, "Organization added successfully");
                populateRegSiteOrgTable();
            }
        }
    }//GEN-LAST:event_addOrgJButtonActionPerformed

