    private void addCountryJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCountryJButtonActionPerformed

        //Validation
        boolean validationSuccess;
        validationSuccess = validation();

        if (validationSuccess) {

            CountryEnterprise objCountryEnterprise = CountryEnterpriseDirectory.checkCountryEnterpriseExists(countryEntName, objWorldEnterprise);

            if (objCountryEnterprise == null) {

                objCountryEnterprise = (CountryEnterprise) objWorldEnterprise.getObjCountryEnterpriseDirectory().createNewCountryEnterprise();
                objCountryEnterprise.setCountryEntName(countryEntName);

                populateCountryTable();                
                JOptionPane.showMessageDialog(null, "Country added sucessfully");                
                countryNameJTextField.setText(null);
            } else {
                JOptionPane.showMessageDialog(null, "Country already exists. Please check or add a proper country");
            }
        }
    }//GEN-LAST:event_addCountryJButtonActionPerformed

    
    private boolean validation() {

        countryEntName = countryNameJTextField.getText().trim();
        
        if (Validation.checkForNull(countryEntName)) {
            JOptionPane.showMessageDialog(null, "Please enter a Country Name");
            countryNameJTextField.requestFocus();
            return false;
        }
        
        if (Validation.checkForOnlyAlphabets(countryEntName)) {
            JOptionPane.showMessageDialog(null, "Please enter a proper country name. It should contain only alphabets");
            countryNameJTextField.requestFocus();
            return false;
        }
        
        return true;
    }
    
    
    private void mngCountryEntAdminJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mngCountryEntAdminJButtonActionPerformed

        CountryEnterprise objCountryEnterprise;

        int selectedCountry = countryEntJTable.getSelectedRow();

        if (selectedCountry < 0) {
            JOptionPane.showMessageDialog(null, "Please select a Product");
            return;
        }

        objCountryEnterprise = (CountryEnterprise) countryEntJTable.getValueAt(selectedCountry, 1);

        if (objCountryEnterprise != null) {

            ManageCountryEnterpriseAdminJPanel objManageCountryEnterpriseAdminJPanel = new ManageCountryEnterpriseAdminJPanel(userProcessContainer, objWorldEnterprise, objCountryEnterprise);

            CardLayoutContainer.addNewCardLayout(userProcessContainer, "ManageCountryEnterpriseAdminJPanel", objManageCountryEnterpriseAdminJPanel);
        } else {
            JOptionPane.showMessageDialog(null, "Please select again");
        }
    }//GEN-LAST:event_mngCountryEntAdminJButtonActionPerformed


