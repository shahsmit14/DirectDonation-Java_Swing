    private void populateCountryTable() {

        DefaultTableModel dtm = (DefaultTableModel) countryEntJTable.getModel();
        dtm.setRowCount(0);

        for (CountryEnterprise objCountryEnterprise : objWorldEnterprise.getObjCountryEnterpriseDirectory().getCountryEnterpriseList()) {

            Object row[] = new Object[2];
            row[0] = objCountryEnterprise.getCountryID();
            row[1] = objCountryEnterprise;

            dtm.addRow(row);
        }
    }

