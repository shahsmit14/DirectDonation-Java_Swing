    private void populateWorldOrgTable() {

        DefaultTableModel dtm = (DefaultTableModel) worldOrgJTable.getModel();
        dtm.setRowCount(0);

        for (Organization objOrganization : objWorldEnterprise.getObjOrganizationDirectory().getOrganizationList()) {

            Object row[] = new Object[2];
            row[0] = objOrganization.getOrganizationID();
            row[1] = objOrganization;

            dtm.addRow(row);
        }
    }

    private void populateWorldOrgComboBox() {

        worlOrgJComboBox.removeAllItems();

        for (Organization.OrganizationType orgType : Organization.OrganizationType.values()) {

            if (orgType.getValue().equals(Organization.OrganizationType.Finance.getValue())
                    || orgType.getValue().equals(Organization.OrganizationType.Support.getValue())) {

                worlOrgJComboBox.addItem(orgType);
            }
        }
    }

