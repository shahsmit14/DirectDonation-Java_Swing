    private void addWorldOrgJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addWorldOrgJButtonActionPerformed

        boolean isOrgPresent = false;

        if (worlOrgJComboBox.getItemCount() != 0) {

            Organization.OrganizationType orgTypeSelected = (Organization.OrganizationType) worlOrgJComboBox.getSelectedItem();

            for (Organization objOrgPresent : objWorldEnterprise.getObjOrganizationDirectory().getOrganizationList()) {

                if (objOrgPresent.getClass().getSimpleName().equals(orgTypeSelected.getValue())) {

                    JOptionPane.showMessageDialog(null, "Organization already exists");
                    isOrgPresent = true;
                    return;
                }
            }

            if (!isOrgPresent) {

                objWorldEnterprise.getObjOrganizationDirectory().addOrganization(orgTypeSelected);
                JOptionPane.showMessageDialog(null, "Organization added successfully");
                populateWorldOrgTable();
            }
        }
    }//GEN-LAST:event_addWorldOrgJButtonActionPerformed

